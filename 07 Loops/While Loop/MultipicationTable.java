public class MultipicationTable {
    public static void main(String[] args) {
        

   int i = 1 ;

   while (i <= 10) 
   {
        System.out.println("Multipication Table " + i);
        i++;

        int j = 1 ;

          while (j <= 10) 
         {
           System.out.println(j*i);
           j++;
         }
   }

    }
}
