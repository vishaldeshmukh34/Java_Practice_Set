public class AllDataTypes {
    public static void main(String[] args) {
        
        // Primitive Data Types
        byte b = 120;              // 1 byte
        short s = 32000;           // 2 bytes
        int i = 100000;            // 4 bytes
        long l = 123456789L;       // 8 bytes
        float f = 3.14f;           // 4 bytes (decimal)
        double d = 99.999;         // 8 bytes (decimal)
        char c = 'A';              // 2 bytes (character)
        boolean bool = true;       // 1 bit (true/false)

        // Non-primitive Data Type
        String str = "Hello Vishal"; // sequence of characters

        // Printing all data types
        System.out.println("byte: " + b);
        System.out.println("short: " + s);
        System.out.println("int: " + i);
        System.out.println("long: " + l);
        System.out.println("float: " + f);
        System.out.println("double: " + d);
        System.out.println("char: " + c);
        System.out.println("boolean: " + bool);
        System.out.println("String: " + str);
    }
}
