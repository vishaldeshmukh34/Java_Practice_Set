public class Assignment {
      

    //this is Assignment Operators

    public static void main(String[] args) {
        int a = 10; 
        a += 5;  // a = a + 5;
        a -= 5;  // a = a - 5;
        a *= 5;  // a = a * 5;
        a /= 5;  //a = a / 5;
        a %= 5; // a = a % 5;

        System.out.println(a);
    }   
}