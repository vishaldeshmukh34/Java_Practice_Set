

public class Program3_AverageOfThreeNumbers {

    //3. WAP to calculate average of three integer nos.

    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = 30;
        
        int sum = a + b + c;

        double average = sum / 3.0; // divide by 3.0 to get decimal result
        System.out.println("Average: " + average);
    }
}
