

// 7. WAP to print the multiplication table of a given number.

public class Program7_MultiplicationTable {
    public static void main(String[] args) {
        int n = 9; // number for which table is to be printed

        for (int i = 1; i <= 10; i++) {
            System.out.println(n + " x " + i + " = " + (n * i));
        }
    }
}
